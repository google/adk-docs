// --8<-- [start:tool_definition]
class CapitalService {
    @Tool(description = "Retrieves the capital city for a given country.")
    fun getCapitalCity(@Param("The country to get capital for.") country: String): String {
        val capitals = mapOf("france" to "Paris", "japan" to "Tokyo", "canada" to "Ottawa")
        return capitals[country.lowercase()] ?: "Sorry, I don't know the capital of $country."
    }
}
// --8<-- [end:tool_definition]

fun main() = runBlocking {
    val capitalService = CapitalService()
    
    // --8<-- [start:identity]
    val capitalAgent = LlmAgent(
        name = "capital_agent",
        model = Gemini(name = "gemini-flash-latest"),
        description = "Answers user questions about the capital city of a given country."
    )
    // --8<-- [end:identity]

    // --8<-- [start:instruction]
