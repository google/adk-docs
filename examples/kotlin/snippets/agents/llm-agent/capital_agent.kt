// --8<-- [start:tool_definition]
class CapitalService {
    @AdkTool(description = "Retrieves the capital city for a given country.")
    fun getCapitalCity(@AdkParam("The country to get capital for.") country: String): String {
        val capitals = mapOf("france" to "Paris", "japan" to "Tokyo", "canada" to "Ottawa")
        return capitals[country.lowercase()] ?: "Sorry, I don't know the capital of $country."
    }
}
// --8<-- [end:tool_definition]

fun main() = runBlocking {
    val capitalService = CapitalService()
    
    // --8<-- [start:identity]
    val capitalAgent = LlmAgent(
        name = "capital_agent",
        model = GeminiModel("gemini-flash-latest"),
        description = "Answers user questions about the capital city of a given country."
    )
    // --8<-- [end:identity]

    // --8<-- [start:instruction]
    val instructedAgent = LlmAgent(
        name = "capital_agent",
        model = GeminiModel("gemini-flash-latest"),
        instruction = Instruction("""
            You are an agent that provides the capital city of a country.
            When a user asks for the capital of a country:
            1. Identify the country name from the user's query.
            2. Use the `getCapitalCity` tool to find the capital.
            3. Respond clearly to the user, stating the capital city.
            Example Query: "What's the capital of {country}?"
            Example Response: "The capital of France is Paris."
        """.trimIndent())
    )
    // --8<-- [end:instruction]

    // --8<-- [start:tool_usage]
    // Note: adkTools() is generated by KSP for classes containing @AdkTool annotated functions.
    // In a real project, you would need to set up the ADK KSP processor.
    // val agentWithTools = LlmAgent(
