    //     tools = capitalService.adkTools() 
    // )
    // --8<-- [end:tool_usage]

    // --8<-- [start:gen_config]
    val agentWithConfig = LlmAgent(
        name = "capital_agent",
        model = GeminiModel("gemini-flash-latest"),
        generateContentConfig = GenerateContentConfig(
            temperature = 0.2f,
            maxOutputTokens = 250
        )
    )
    // --8<-- [end:gen_config]

    // --8<-- [start:full_example]
    val finalAgent = LlmAgent(
        name = "capital_agent",
        model = GeminiModel("gemini-flash-latest"),
        description = "Answers user questions about the capital city of a given country.",
        instruction = Instruction("You are an agent that provides the capital city of a country...")
        // tools = capitalService.adkTools() // Assuming tools are added
